  void _shuffleQuestions() {
    final indices = List<int>.generate(questions.length, (i) => i);
    indices.shuffle();
    setState(() {
      _shuffledIndices = indices;
      userAnswers = List.filled(questions.length, -1); // ‡∏£‡∏µ‡πÄ‡∏ã‡πá‡∏ï‡∏Ñ‡∏≥‡∏ï‡∏≠‡∏ö
    });
