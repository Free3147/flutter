  Future<void> _checkIfAlreadySubmitted() async {
    final user = FirebaseAuth.instance.currentUser;
    if (user == null) return;

    try {
      final doc = await FirebaseFirestore.instance.collection('progress').doc(user.uid).get();
      if (doc.exists) {
        final data = doc.data() as Map<String, dynamic>;
        if (data['preTestCompleted'] == true) {
          setState(() {
            _submitted = true;
          });
        }
      }
    } catch (e) {
      print('Error checking pre-test status: $e');
    }
  }

  Future<void> _submitAnswers() async {
    int score = 0;
    // üîπ ‡πÉ‡∏ä‡πâ index ‡πÄ‡∏î‡∏¥‡∏°‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏ï‡∏£‡∏ß‡∏à‡∏Ñ‡∏≥‡∏ï‡∏≠‡∏ö
    for (int i = 0; i < questions.length; i++) {
      if (userAnswers[i] == correctAnswers[i]) {
        score++;
      }
    }

    final user = FirebaseAuth.instance.currentUser;
    if (user != null) {
      await FirebaseFirestore.instance.collection('test_results').add({
        'userId': user.uid,
        'type': 'pre_test',
        'score': score,
        'total': 20,
        'percentage': (score / 20) * 100,
        'timestamp': FieldValue.serverTimestamp(),
      });

      await FirebaseFirestore.instance
          .collection('progress')
          .doc(user.uid)
          .set({
        'preTestCompleted': true,
        'preTestScore': score,
        'preTestTotal': 20,
        'lastUpdated': FieldValue.serverTimestamp(),
      }, SetOptions(merge: true));
    }

    // üîπ ‡∏Å‡∏•‡∏±‡∏ö‡πÑ‡∏õ‡∏´‡∏ô‡πâ‡∏≤ Home ‡∏ó‡∏±‡∏ô‡∏ó‡∏µ (‡πÑ‡∏°‡πà‡πÅ‡∏™‡∏î‡∏á‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô)
